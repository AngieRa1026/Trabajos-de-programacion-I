
public class AsignarVariables2 {

    public static void main(String[] args) {
        // Declaracion y asignacion de variables
        byte numEdad = 20;
        float numAltura = 1.64f; // en metros
        char inicialNombre = 'A';
        boolean esMayorDeEdad = true;

        // Mostrar los valores de las variables
        System.out.println("Edad: " + numEdad);
        System.out.println("Altura: " + numAltura + " metros");
        System.out.println("Inicial del nombre: " + inicialNombre);
        System.out.println("Es mayor de edad: " + esMayorDeEdad);
    }
}
