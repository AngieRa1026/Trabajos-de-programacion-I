public class Ejercicio14TablaDeMultiplicarDel5 {

    public static void main(String[] args) {
        System.out.println("La tabla de multiplicar del 5 es : ");
        for (int i = 1; i <= 10; i++) {
            System.out.println(5 + " x " + i + " = " + 5 * i);

        }
    }
}
