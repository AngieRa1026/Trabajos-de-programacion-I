package unidad1;

public class Comparacion {

    public static void main(String[] args) {
        int numero1 = 10;
        int numero2 = 20;

        System.out.println("¿numero 1 es igual a numero 2? " + (numero1 == numero2));
        System.out.println("¿numero 1 es diferente de numero 2? " + (numero1 != numero2));
        System.out.println("¿numero 1 es mayor que numero 2? " + (numero1 > numero2));
        System.out.println("¿numero 1 es menor que numero 2? " + (numero1 < numero2));
        System.out.println("¿numero 1 es mayor o igual que numero 2? " + (numero1 >= numero2));
        System.out.println("¿numero 1 es menor o igual que numero 2? " + (numero1 <= numero2));
    }
}
